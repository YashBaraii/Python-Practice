# Convert string to uppercase, lowercase, title case.

st = "Hey, myself Yash, I live in Los Angelas"

print(f"String: {st}\n")


uppercase_st = st.upper()
lowercase_st = st.lower()
titleCase_st = st.title()

print(f"Uppercase version: {uppercase_st}")
print(f"Lowercase version: {lowercase_st}")
print(f"Title Case version: {titleCase_st}")
